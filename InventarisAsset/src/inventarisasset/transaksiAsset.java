     public void tampilDataTransaksi() {
    try {
            asset_transaction.setRowCount(0);

            String query = "SELECT * FROM asset_transaction";
            try (PreparedStatement preparedStatement = obj.konek.prepareStatement(query);
                 ResultSet data = preparedStatement.executeQuery()) {
                while (data.next()) {
                    Object[] line = new Object[5];
                    line[0] = data.getString("transaction_id");
                    line[1] = data.getString("asset_id");
                    line[2] = data.getString("transaction_type");
                    line[3] = data.getString("transaction_date");
                    line[4] = data.getString("transaction_amount");
                    asset_transaction.addRow(line);
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    }
    }

