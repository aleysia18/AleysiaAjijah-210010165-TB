    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
        if (txtIDtrans.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "ID Transaksi belum diisi");
            txtIDtrans.requestFocus();
        } else if (txtIDasset.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "ID Asset belum diisi");
            txtIDasset.requestFocus();
        } else if (txtTipeTrans.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Tipe Transaksi belum diisi");
            txtTipeTrans.requestFocus();
        } else if (txtTGLtrans.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Tanggal Transaksi belum diisi");
            txtTGLtrans.requestFocus();
        } else if (txtJMLHtrans.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Jumlah Transaksi belum diisi");
            txtJMLHtrans.requestFocus();
        } else {
            String sql = "INSERT INTO asset_transaction VALUES (?, ?, ?, ?, ?)";
            try (PreparedStatement preparedStatement = obj.konek.prepareStatement(sql)) {
                preparedStatement.setString(1, txtIDtrans.getText());
                preparedStatement.setString(2, txtIDasset.getText());
                preparedStatement.setString(3, txtTipeTrans.getText());
                preparedStatement.setString(4, txtTGLtrans.getText());
                preparedStatement.setString(5, txtJMLHtrans.getText());
                preparedStatement.executeUpdate();
                JOptionPane.showMessageDialog(this, "Data berhasil disimpan");
                // Kosongkan field setelah penyimpanan
                txtIDtrans.setText("");
                txtIDasset.setText("");
                txtTipeTrans.setText("");
                txtTGLtrans.setText("");
                txtJMLHtrans.setText("");
            }
        }
        tampilDataTransaksi();
    } catch (SQLException | HeadlessException e) {
        JOptionPane.showMessageDialog(this, e.toString());
    }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try {
        if (txtIDtrans.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Pilih data yang akan dihapus");
        } else {
            String idTransaksi = txtIDtrans.getText();
            String sql = "DELETE FROM asset_transaction WHERE transaction_id = ?";
            
            try (PreparedStatement preparedStatement = obj.konek.prepareStatement(sql)) {
                preparedStatement.setString(1, idTransaksi);
                int result = preparedStatement.executeUpdate();

                if (result > 0) {
                    JOptionPane.showMessageDialog(this, "Data berhasil dihapus");
                    // Kosongkan field setelah penghapusan
                    txtIDtrans.setText("");
                    txtIDasset.setText("");
                    txtTipeTrans.setText("");
                    txtTGLtrans.setText("");
                    txtJMLHtrans.setText("");
                } else {
                    JOptionPane.showMessageDialog(this, "Gagal menghapus data");
                }
            }
            tampilDataTransaksi();
        }
    } catch (SQLException | HeadlessException e) {
        JOptionPane.showMessageDialog(this, e.toString());
    }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        try {
        if (txtIDtrans.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Pilih data yang akan diubah");
        } else {
            String idTransaksi = txtIDtrans.getText();
            String idAsset = txtIDasset.getText();
            String tipeTransaksi = txtTipeTrans.getText();
            String tglTransaksi = txtTGLtrans.getText();
            String jumlahTransaksi = txtJMLHtrans.getText();

            String sql = "UPDATE asset_transaction SET asset_id = ?, transaction_type = ?, transaction_date = ?, transaction_amount = ? WHERE transaction_id = ?";
            
            try (PreparedStatement preparedStatement = obj.konek.prepareStatement(sql)) {
                preparedStatement.setString(1, idAsset);
                preparedStatement.setString(2, tipeTransaksi);
                preparedStatement.setString(3, tglTransaksi);
                preparedStatement.setString(4, jumlahTransaksi);
                preparedStatement.setString(5, idTransaksi);
                
                int result = preparedStatement.executeUpdate();

                if (result > 0) {
                    JOptionPane.showMessageDialog(this, "Data berhasil diubah");
                    // Kosongkan field setelah pengeditan
                    txtIDtrans.setText("");
                    txtIDasset.setText("");
                    txtTipeTrans.setText("");
                    txtTGLtrans.setText("");
                    txtJMLHtrans.setText("");
                } else {
                    JOptionPane.showMessageDialog(this, "Gagal mengubah data");
                }
            }
            tampilDataTransaksi();
        }
    } catch (SQLException | HeadlessException e) {
        JOptionPane.showMessageDialog(this, e.toString());
    }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        String path = ".\\src\\report\\reportTransaksi.jasper";
        try {
        // Mengatur koneksi database (gantilah parameter koneksi dengan yang sesuai)
        Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/inventaris_aset", "root", "");

        // Mengisi laporan dengan data dari database
        JasperPrint jprint = JasperFillManager.fillReport(path, null, conn);

        // Menampilkan laporan menggunakan JasperViewer
        JasperViewer jviewer = new JasperViewer(jprint, false);
        jviewer.setDefaultCloseOperation(DISPOSE_ON_CLOSE);
        jviewer.setVisible(true);

        // Menutup koneksi database setelah selesai
        conn.close();
     } catch (JRException | SQLException e) {
        // Menangkap dan menampilkan exception
        e.printStackTrace();
        JOptionPane.showMessageDialog(null, "Error: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
     }
    }//GEN-LAST:event_jButton4ActionPerformed

