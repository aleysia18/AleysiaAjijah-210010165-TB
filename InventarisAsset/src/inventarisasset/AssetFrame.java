    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        try {
           if (txtIDasset.getText().isEmpty()) {
               JOptionPane.showMessageDialog(this, "ID Asset belum diisi");
               txtIDasset.requestFocus();
           } else if (txtNamaAsset.getText().isEmpty()) {
               JOptionPane.showMessageDialog(this, "Nama Asset belum diisi");
               txtNamaAsset.requestFocus();
           } else if (txtTGLpembelian.getText().isEmpty()) {
               JOptionPane.showMessageDialog(this, "Tanggal Pembelian belum diisi");
               txtTGLpembelian.requestFocus(); 
           } else if (txtBiayaPembelian.getText().isEmpty()) {
               JOptionPane.showMessageDialog(this, "Biaya Pembelian belum diisi");
               txtBiayaPembelian.requestFocus();
           } else if (txtNilaiSkrng.getText().isEmpty()) {
               JOptionPane.showMessageDialog(this, "Nilai Sekarang belum diisi");
               txtNilaiSkrng.requestFocus();
           } else {
               String sql = "insert into master_asset value('"+txtIDasset.getText()+"','"+txtNamaAsset.getText()+"','"+txtTGLpembelian.getText()+"','"+txtBiayaPembelian.getText()+"','"+txtNilaiSkrng.getText()+"')";;
               obj.konek.createStatement().execute(sql);
               JOptionPane.showMessageDialog(this, "data berhasil disimpan");
           }
           tampilDataAsset();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.toString());
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
         try {
        if (txtIDasset.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Pilih data yang akan dihapus");
        } else {
            String idAsset = txtIDasset.getText();
            String sql = "DELETE FROM master_asset WHERE asset_id = '" + idAsset + "'";
            int result = obj.konek.createStatement().executeUpdate(sql);

            if (result > 0) {
                JOptionPane.showMessageDialog(this, "Data berhasil dihapus");
                // Kosongkan field setelah penghapusan
                txtIDasset.setText("");
                txtNamaAsset.setText("");
                txtTGLpembelian.setText("");
                txtBiayaPembelian.setText("");
                txtNilaiSkrng.setText("");
            } else {
                JOptionPane.showMessageDialog(this, "Gagal menghapus data");
            }
            tampilDataAsset();
        }
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, e.toString());
    }
    }//GEN-LAST:event_btnHapusActionPerformed

