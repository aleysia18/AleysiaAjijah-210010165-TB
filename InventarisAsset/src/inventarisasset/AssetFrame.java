    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        try {
        if (txtIDasset.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Pilih data yang akan diubah");
        } else {
            String idAsset = txtIDasset.getText();
            String namaAsset = txtNamaAsset.getText();
            String tglPembelian = txtTGLpembelian.getText();
            String biayaPembelian = txtBiayaPembelian.getText();
            String nilaiSkrng = txtNilaiSkrng.getText();

            String sql = "UPDATE master_asset SET asset_name='" + namaAsset + "', purchase_date='" + tglPembelian + "', purchase_cost='" + biayaPembelian + "', current_value='" + nilaiSkrng + "' WHERE asset_id='" + idAsset + "'";
            int result = obj.konek.createStatement().executeUpdate(sql);

            if (result > 0) {
                JOptionPane.showMessageDialog(this, "Data berhasil diubah");
                // Kosongkan field setelah pengeditan
                txtIDasset.setText("");
                txtNamaAsset.setText("");
                txtTGLpembelian.setText("");
                txtBiayaPembelian.setText("");
                txtNilaiSkrng.setText("");
            } else {
                JOptionPane.showMessageDialog(this, "Gagal mengubah data");
            }
            tampilDataAsset();
        }
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, e.toString());
    }
    }//GEN-LAST:event_btnEditActionPerformed

    private void btnCetakActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCetakActionPerformed
        String path = ".\\src\\report\\reportAsset.jasper";
        try {
        // Mengatur koneksi database (gantilah parameter koneksi dengan yang sesuai)
        Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/inventaris_aset", "root", "");

        // Mengisi laporan dengan data dari database
        JasperPrint jprint = JasperFillManager.fillReport(path, null, conn);

        // Menampilkan laporan menggunakan JasperViewer
        JasperViewer jviewer = new JasperViewer(jprint, false);
        jviewer.setDefaultCloseOperation(DISPOSE_ON_CLOSE);
        jviewer.setVisible(true);

        // Menutup koneksi database setelah selesai
        conn.close();
     } catch (JRException | SQLException e) {
        // Menangkap dan menampilkan exception
        e.printStackTrace();
        JOptionPane.showMessageDialog(null, "Error: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
     }
    }//GEN-LAST:event_btnCetakActionPerformed

