    public void tampilDataAsset() {
    try {
            master_asset.setRowCount(0);

            String query = "SELECT * FROM master_asset";
            try (PreparedStatement preparedStatement = obj.konek.prepareStatement(query);
                 ResultSet data = preparedStatement.executeQuery()) {
                while (data.next()) {
                    Object[] line = new Object[5];
                    line[0] = data.getString("asset_id");
                    line[1] = data.getString("asset_name");
                    line[2] = data.getString("purchase_date");
                    line[3] = data.getString("purchase_cost");
                    line[4] = data.getString("current_value");
                    master_asset.addRow(line);
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    }
    }

